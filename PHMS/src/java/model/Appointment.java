package model;

import java.sql.Timestamp;

public class Appointment {
    private int apptId;
    private int petId;
    private int vetId;
    private Timestamp startTime;
    private String status; // Pending, Confirmed, Completed, Cancelled, Pending Payment
    private String type; // Checkup, Surgery, Urgent
    private String petName;
    private String vetName;
    private String serviceName;
    private int serviceId;
    private Timestamp createdAt;
    private String ownerEmail;

    public Appointment() {
    }

    public Appointment(int apptId, int petId, int vetId, Timestamp startTime, String status, String type) {
        this.apptId = apptId;
        this.petId = petId;
        this.vetId = vetId;
        this.startTime = startTime;
        this.status = status;
        this.type = type;
    }

    public int getApptId() {
        return apptId;
    }

    public void setApptId(int apptId) {
        this.apptId = apptId;
    }

    public int getPetId() {
        return petId;
    }

    public void setPetId(int petId) {
        this.petId = petId;
    }

    public int getVetId() {
        return vetId;
    }

    public void setVetId(int vetId) {
        this.vetId = vetId;
    }

    public Timestamp getStartTime() {
        return startTime;
    }

    public void setStartTime(Timestamp startTime) {
        this.startTime = startTime;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPetName() {
        return petName;
    }

    public void setPetName(String petName) {
        this.petName = petName;
    }

    public String getVetName() {
        return vetName;
    }

    public void setVetName(String vetName) {
        this.vetName = vetName;
    }

    public String getServiceName() {
        return serviceName;
    }

    public void setServiceName(String serviceName) {
        this.serviceName = serviceName;
    }

    public int getServiceId() {
        return serviceId;
    }

    public void setServiceId(int serviceId) {
        this.serviceId = serviceId;
    }

    public Timestamp getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Timestamp createdAt) {
        this.createdAt = createdAt;
    }

    public String getOwnerEmail() {
        return ownerEmail;
    }

    public void setOwnerEmail(String ownerEmail) {
        this.ownerEmail = ownerEmail;
    }
}
